/**
 * Returns ~40 inventory items for a given pharmacy with realistic stock levels.
 * Cycles through MEDICINES and attaches pharmacy-specific pricing.
 */
export function getPharmacyInventory(pharmacyId: string): MockInventoryItem[] {
  // Use pharmacyId to deterministically vary stock and pricing
  const seed = pharmacyId.charCodeAt(pharmacyId.length - 1);

  return MEDICINES.slice(0, 40).map((med, idx) => {
    // Simulate some items being out of stock (~10%)
    const isListed = (seed + idx) % 11 !== 0;
    // Discount between 0 and 20%
    const discountBps = ((seed + idx) % 5) * 500; // 0, 500, 1000, 1500, 2000 bps
    const sellingPrice = Math.round(med.mrp_paise * (1 - discountBps / 10000));
    const qtyAvailable = isListed ? 5 + ((seed + idx * 3) % 50) : 0;

    return {
      ...med,
      pharmacy_id: pharmacyId,
      qty_available: qtyAvailable,
      selling_price_paise: sellingPrice,
      discount_bps: discountBps,
      is_listed: isListed,
    };
  });
}

/**
 * Filters MEDICINES by brand_name, generic_name, or category.
 * Case-insensitive substring match.
 */
export function searchMedicines(query: string): MockMedicine[] {
  if (!query.trim()) return MEDICINES;
  const q = query.toLowerCase().trim();
  return MEDICINES.filter(
    (m) =>
      m.brand_name.toLowerCase().includes(q) ||
      m.generic_name.toLowerCase().includes(q) ||
      m.category.toLowerCase().includes(q),
  );
}
